"""
MoLoRAG Baseline: Evaluation Script
-----------------------------------
Calculates performance metrics (EM, Accuracy, Binary Correctness) 
for the QA outputs generated by the baseline VLM script.
"""

import os 
import json 
import argparse
from tqdm import tqdm
from evaluate.eval_qa import (
    extract_answer, extract_score, eval_one_sample, 
    eval_samples, show_fine_grained_results
)

def run_evaluation(args):
    """Executes the evaluation pipeline on predicted output folders."""
    
    # Determine prediction paths based on method
    if args.method == "LLM":
        pred_folder = f"./results/{args.dataset}/LLM"
        pred_paths = [f"{pred_folder}/{args.model_name}_top{args.topk}.json"]
    elif args.method == "VLM":
        pred_folder = f"./results/{args.dataset}/{args.model_name}"
        if args.retriever != "None":
            pred_paths = [f"{pred_folder}/{args.retriever}_top{args.topk}.json"]
        else:
            pred_paths = [f"{pred_folder}/Direct.json"]
    elif args.method == "MDocAgent": 
        pred_folder = f"./results/{args.dataset}/mdocagent"
        pred_paths = [f"{pred_folder}/_.json"]
        args.folder_eval = True
    
    if args.folder_eval: 
        # Multi-file evaluation mode
        if not os.path.exists(pred_folder):
            print(f"[ERROR] Evaluation folder {pred_folder} missing.")
            return
        pred_paths = sorted([f"{pred_folder}/{file}" for file in os.listdir(pred_folder) if file.endswith(".json")])
        pred_paths = [path for path in pred_paths if "_scored" not in path]
        
    # Load scoring prompts
    try:
        with open("./evaluate/prompt_for_answer_extraction.md", 'r') as f:
            extractor_prompt = f.read()
        with open("./evaluate/prompt_for_scoring.md", 'r') as file:
            scoring_prompt = file.read()
    except FileNotFoundError as e:
        print(f"[!] Warning: Scoring prompts missing. Extraction might fail: {e}")
        extractor_prompt, scoring_prompt = "", ""
    
    for cur_pred_path in pred_paths:
        if not os.path.exists(cur_pred_path):
            print(f"[!] Warning: Prediction file {cur_pred_path} not found.")
            continue
            
        print(f"[*] Scoring: {cur_pred_path}")
        raw_samples = json.load(open(cur_pred_path, 'r'))
        scored_samples, scored_ids = [], set()
        rewrite_pred_path = cur_pred_path.replace(".json", "_scored.json")
        
        # Resume progress from existing scored file
        if os.path.exists(rewrite_pred_path): 
            scored_samples = json.load(open(rewrite_pred_path, 'r')) 
            scored_ids = set([s.get("id", s.get("doc_id")) for s in scored_samples])
            
        # Evaluation loop
        for sample in tqdm(raw_samples, desc="Evaluating Samples"):
            sample_id = sample.get("id", sample.get("doc_id"))
            if sample_id in scored_ids:
                continue 

            # MMLong/LongDocURL use text-based answer extraction
            if args.dataset in ["MMLong", "LongDocURL"]: 
                if "pred_ans" not in sample:
                    extracted_ans = extract_answer(
                        question=sample["question"], 
                        output=sample.get("raw_response", ""),
                        extractor_prompt=extractor_prompt
                    )
                    try:
                        # Extract clean answer segment
                        pred_ans = extracted_ans.split("Answer format:")[0].split("Extracted answer:")[1].strip()
                        sample["pred_ans"] = pred_ans
                    except:
                        sample["pred_ans"] = "Failed to extract"
                
                em_score, acc_score = eval_one_sample(
                    gt=sample["answer"], 
                    pred=sample["pred_ans"], 
                    answer_type=sample["answer_format"]
                )
                sample["score"] = {"EM": em_score, "Acc": acc_score}
                
            # Table datasets use direct binary scoring
            elif args.dataset in ["PaperTab", "FetaTab"]: 
                if "score" not in sample: 
                    generated_score = extract_score(
                        question=sample["question"], 
                        output=sample.get("raw_response", ""),
                        ground_truth=sample["answer"],
                        prompt=scoring_prompt
                    )
                    score = generated_score.get('binary_correctness', 0) 
                    sample["score"] = {"BinaryCorrectness": score}
            
            scored_samples.append(sample)  
            if len(scored_samples) % args.save_freq == 0: 
                with open(rewrite_pred_path, 'w') as file:
                    json.dump(scored_samples, file, indent=4, sort_keys=True)
        
        # Final metrics reporting
        with open(rewrite_pred_path, 'w') as file:
            json.dump(scored_samples, file, indent=4, sort_keys=True)

        score_dict = eval_samples(scored_samples, args.dataset)
        print(f"\n[METRICS] Method: {args.method} | Model: {args.model_name} | Top-{args.topk}")

        if args.dataset in ["MMLong", "LongDocURL"]: 
            show_fine_grained_results(scored_samples, args.dataset)
        else:
            print(f"Scores: {score_dict}")
        
    print(f"\n[DONE] Evaluation complete.\n")


if __name__ == "__main__":
    parser = argparse.ArgumentParser() 
    parser.add_argument("--dataset", type=str, default="MMLong")
    parser.add_argument("--method", type=str, default="VLM", choices=["LLM", "VLM", "MDocAgent"])
    parser.add_argument("--model_name", type=str, default="QwenVL-7B", choices=[
        "qwen-7b", "mistral-7b", "llama-8b", "deepseek-chat", "gpt-4o-mini",
        "QwenVL-7B", "QwenVL-3B", "DeepSeek-VL-tiny", "DeepSeek-VL-small", 
        "LLaVA-Next-7B", "LLaVA-Next-8B", "LLaMA-VL-11B"
    ])
    parser.add_argument("--topk", type=int, default=5)
    parser.add_argument("--retriever", type=str, default="None", choices=["None", "base", "beamsearch", "beamsearch_LoRA"])
    parser.add_argument("--folder_eval", action="store_true", default=False)
    parser.add_argument("--save_freq", type=int, default=50)
    args = parser.parse_args()
    
    run_evaluation(args)
